(function () {
  var STORAGE_KEY = "lumena_lang";

  function getLang() {
    var stored = null;
    try { stored = localStorage.getItem(STORAGE_KEY); } catch (e) {}
    return stored === "en" ? "en" : "es";
  }

  function t(key, lang) {
    var dict = window.LUMENA_I18N[lang] || window.LUMENA_I18N.es;
    return dict[key] !== undefined ? dict[key] : (window.LUMENA_I18N.es[key] || key);
  }

  function applyI18n(lang) {
    document.documentElement.setAttribute("lang", lang);

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      el.textContent = t(el.getAttribute("data-i18n"), lang);
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      el.innerHTML = t(el.getAttribute("data-i18n-html"), lang);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder"), lang));
    });
    document.querySelectorAll("[data-i18n-aria]").forEach(function (el) {
      el.setAttribute("aria-label", t(el.getAttribute("data-i18n-aria"), lang));
    });
    document.querySelectorAll(".lang-toggle button").forEach(function (btn) {
      btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
    });

    document.dispatchEvent(new CustomEvent("lumena:langchange", { detail: { lang: lang } }));
  }

  function setLang(lang) {
    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
    applyI18n(lang);
  }

  window.LumenaI18n = { t: t, getLang: getLang, setLang: setLang, applyI18n: applyI18n };

  document.addEventListener("DOMContentLoaded", function () {
    var lang = getLang();
    applyI18n(lang);
    document.querySelectorAll(".lang-toggle button").forEach(function (btn) {
      btn.addEventListener("click", function () { setLang(btn.getAttribute("data-lang")); });
    });
  });
})();
